#ifndef STACKFUN_H
#define STACKFUN_H

#include <stack>
#include <queue>

using std::stack;
using std::queue;

class StackFun
{
    public:
    
    template <typename T>
    static T sum(stack<T> & s);
    
    template <typename T>
    static void scramble(queue<T> & q);

    template <typename T>
    static bool verifySame(stack<T> & s, queue<T> & q);
};

#include "stackfun.cpp"
#endif
